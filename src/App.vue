<template>
  <div id="app">
    <code-mirror
      v-model="vueSource"
      title="Vue Component(editable)"
    />
    <code-mirror
      ref="consumeMirror"
      v-model="consumerSource"
      title="Generated content"
      :mode="consumerMode"
      theme="darcula"
      read-only
    >
      <button
        type="button"
        :class="{active: currentType === 0}"
        @click="handleSwitch(0)"
      >
        Raw Markdown
      </button>
      <button
        type="button"
        :class="{active: currentType === 1}"
        @click="handleSwitch(1)"
      >
        Parser Result
      </button>
      <button
        type="button"
        :class="{active: currentType === 2}"
        @click="handleSwitch(2)"
      >
        Render Result
      </button>
      <a
        class="githubLink"
        href="https://github.com/vuese/vuese-explorer"
      >
        ðŸ”— Github
      </a>
    </code-mirror>
  </div>
</template>

<script>
import vueSource from '@/utils/vueSource'
import { stringify } from '@/utils/tools'
import { parser } from '@vuese/parser'
import Render from '@vuese/markdown-render'

export default {
  components: {
    CodeMirror: () => import('@/components/CodeMirror')
  },
  data() {
    return {
      vueSource,
      parserRes: null,
      renderRes: null,
      md: '',
      currentType: 0,
      consumerMode: 'markdown',
      consumerSource: ''
    }
  },
  watch: {
    vueSource: 'make'
  },
  mounted() {
    this.make()
  },
  methods: {
    make() {
      try {
        this.parserRes = parser(this.vueSource)
        const VR = new Render(this.parserRes)
        this.mdRes = VR.renderMarkdown()
        this.renderRes = VR.render()

        this.handleSwitch(this.currentType)
      } catch (e) {
        this.consumerSource = e.toString()
      }
    },
    handleSwitch(type) {
      console.log(type)
      this.currentType = type
      switch (type) {
        // Raw markdown
        case 0:
          this.consumerSource = this.mdRes ? this.mdRes.content : ''
          this.consumerMode = 'markdown'
          break
        // Parser result
        case 1:
          this.consumerSource = stringify(this.parserRes)
          this.consumerMode = 'javascript'
          break
        // Render result
        case 2:
          this.consumerSource = stringify(this.renderRes)
          this.consumerMode = 'javascript'
          break
      }
    }
  }
}
</script>

